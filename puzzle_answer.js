//Across 1
const correctWord_A1 = 'horus'
const itemsToBeDisplayed_A1 = document.querySelectorAll('.id_A1')
const form_A1 = document.getElementById('form2')

form_A1.addEventListener('submit', e => {
	e.preventDefault()
	let inputText_A1 = document.getElementById('text_A1').value

	if (inputText_A1 === '' || inputText_A1 === null) return
	if (inputText_A1 === correctWord_A1) {
		itemsToBeDisplayed_A1.forEach(item => {
			item.style.color = 'rgb(255, 255, 255)'
		})
	}
})
